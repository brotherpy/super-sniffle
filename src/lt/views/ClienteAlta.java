    private void recuperarCiudades() {
         daoDistrito = new DaoCiudad();
        ciudades = new ArrayList<>();
        distritos = daoDistrito.recuperarTodo();
              for (int i = 0; i < distritos.size(); i++) {
            ciudades.add(distritos.get(i).getCiudades());

        }
        cbCiudades.setModel(new DefaultComboBoxModel(ciudades.toArray()));

    }


