    private void guardar(){
      daoDistrito = new DaoCiudad();
        distrito = daoDistrito.recuperarDistritosPorParametro(cbCiudades.getSelectedItem().toString());
            cliente = new Cliente();
  
        cliente.setNombre(txNombre.getText());
        cliente.setApellido(txApellido.getText());
        cliente.setCin(Integer.parseInt(txCedula.getText()));
        cliente.setTelefono(txTelefono.getText());
        cliente.setDireccion(txDireccion.getText());
        cliente.setDistrito(distrito);
        
        
        dao = new DaoCliente();
    
             
        try {
            dao.insertar(cliente);
        } catch (Exception e) {
            dao.rollback();
              String error = "org.hibernate.exception.ConstraintViolationException: could not execute statement";
            if (e.toString().equals(error)) {
                JOptionPane.showMessageDialog(null, "Ya se ha registrado un cliente con la cédula: "+txCedula.getText()+"\n"+" Favor verifique");
                txCedula.requestFocus();
            }else{
            JOptionPane.showMessageDialog(null, "Se ha producido un error en la carga de datos" );
            } ;
        }
        dao.commit();
       
      
           dispose();
           
             }
           
            
      
    
    

  
