    private void eliminar() {
        int index = jTable1.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Vehículo");
            return;
        }
        vehiculo = vehiculoslist.get(index);
        int result = JOptionPane.showConfirmDialog(null,
                "Está  seguro en eliminar el vehículo"
                + vehiculo.getMarca() + " " + "con placa n° " + vehiculo.getPlaca(),
                "Atención", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            daoVehiculo = new DaoVehiculo();
            try {
                daoVehiculo.eliminar(vehiculo);
                daoVehiculo.commit();
                recuperarTodo();
                vaciarCampos();
            } catch (Exception e) {
                daoVehiculo.rollback();
                JOptionPane.showMessageDialog(null, "Operación no permitida, no se puede eliminar un Vehículo asegurado. \n");
            }
        }

    }

