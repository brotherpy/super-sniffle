        vehiculo.setPlaca(txPlaca.getText());
        vehiculo.setNumeroMotor(txNumeroMotor.getText());
        vehiculo.setNumeroSerie(txNroSerie.getText());
        vehiculo.setCilindrada(txCilindrada.getText());
