    private void seleccionarObjeto(int index) {
        if (index < 0) {
            return;
        }

        cliente = listaCliente.get(index);
        daoDepartamento = new DaoDepartamento();
        departamento = daoDepartamento.recuperarPorId(cliente.getDistrito().getDepartamentoId().getId());

        distrito = cliente.getDistrito();
        recuperarDepartamentos();
        cbDepartamento.setSelectedItem(departamento.getDepartamentos());
        mostrarCiudades();
        cbCiudades.setSelectedItem(distrito.getCiudades());
        lblCodigo.setText(cliente.getCodigo() + "");
        txNombre.setText(cliente.getNombre());
        txApellido.setText(cliente.getApellido());
        txCedula.setText(cliente.getCin() + "");
        txTelefono.setText(cliente.getTelefono() + "");
        txDireccion.setText(cliente.getDireccion());
        daoVehiculo = new DaoVehiculo();
        List<Vehiculo> vehiculos = daoVehiculo.recuperarVehiculoPorIdCliente(cliente);
        mtVehiculoCliente.setLista(vehiculos);
        jTable2.setModel(mtVehiculoCliente);

        estadoBotonesSeleccionarObjetos();

    }

    private void guardar() {
        daoDistrito = new DaoCiudad();
        distrito = daoDistrito.recuperarDistritosPorParametro(cbCiudades.getSelectedItem().toString());
        if (accion.equals("NUEVO")) {
            cliente = new Cliente();
        }
        cliente.setNombre(txNombre.getText());
        cliente.setApellido(txApellido.getText());
        cliente.setCin(Integer.parseInt(txCedula.getText()));
        cliente.setTelefono(txTelefono.getText());
        cliente.setDistrito(distrito);
        cliente.setDireccion(txDireccion.getText());

        dao = new DaoCliente();
        try {
            if (accion.equals("NUEVO")) {
                dao.insertar(cliente);
            } else {
                dao.actualizar(cliente);
            }
            dao.commit();

            recuperarTodo();
            estadoInicial(true);
            vaciarCampos();
        } catch (Exception e) {
            dao.rollback();
            String error = "org.hibernate.exception.ConstraintViolationException: could not execute statement";
            if (e.toString().equals(error)) {
                JOptionPane.showMessageDialog(null, "Ya se ha registrado un cliente con la cédula: " + txCedula.getText() + "\n" + " Favor verifique");
                txCedula.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Se ha producido un error en la carga de datos");
            };
        }
        txBuscar.setEnabled(true);
        txBuscar.requestFocus();
    }

    private boolean validar() {
        boolean validar = false;
        if (txNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No se puede guardar un cliente sin Nombre, favor verifique.");
            txNombre.requestFocus();
        } else if (txApellido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No se puede guardar un cliente sin Apellido, favor verifique.");
            txApellido.requestFocus();
        } else if (txCedula.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No se puede guardar un cliente sin Documento, favor verifique.");
            txCedula.requestFocus();
        } else if (txDireccion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No se puede guardar un cliente sin Domicilio, favor verifique.");
            txDireccion.requestFocus();
        } else if (txTelefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No se puede guardar un cliente sin N° de teléfono , favor verifique.");
            txTelefono.requestFocus();
        } else {
            validar = true;
        }

        return validar;

    }

