    private void buscar() {
        dao = new DaoCliente();


        String dato = txBuscar.getText();
        
        boolean codigo = false;
        for (int i = 0; i < dato.length(); i++) {
            if (Character.isDigit(dato.charAt(i))) {
                codigo = true;
            }
        }
        if (codigo) {
            dao.recuperarPorId(Integer.parseInt(dato));

        } else {
            listaCliente = dao.recuperarPorFiltro(dato);

        }

        mtCliente.setLista(listaCliente);
        btnCancelar.setEnabled(true);
    }

